#[derive(Debug, PartialEq)]
#[repr(u8)]
pub enum Opcode {
    Constant = 0,
    IConst0,
    IConst1,
    IConst2,
    IConst3,
    IConst4,
    IAdd,
    ISub,
    IMul,
    IDiv,
    FAdd,
    FSub,
    FMul,
    FDiv,
    I2F,
    F2I,
    Invert,
    StrConcat,
    T,
    F,
    And,
    Or,
    Negate,
    LT,
    LTE,
    GT,
    GTE,
    Eq,
    Neq,
    MkArr,
    Store,
    Load,
    Return,
}

impl From<u8> for Opcode {
    fn from(i: u8) -> Self {
        match i {
            0 => Opcode::Constant,
            1 => Opcode::IConst0,
            2 => Opcode::IConst1,
            3 => Opcode::IConst2,
            4 => Opcode::IConst3,
            5 => Opcode::IConst4,
            6 => Opcode::IAdd,
            7 => Opcode::ISub,
            8 => Opcode::IMul,
            9 => Opcode::IDiv,
            10 => Opcode::FAdd,
            11 => Opcode::FSub,
            12 => Opcode::FMul,
            13 => Opcode::FDiv,
            14 => Opcode::I2F,
            15 => Opcode::F2I,
            16 => Opcode::Invert,
            17 => Opcode::StrConcat,
            18 => Opcode::T,
            19 => Opcode::F,
            20 => Opcode::And,
            21 => Opcode::Or,
            22 => Opcode::Negate,
            23 => Opcode::LT,
            24 => Opcode::LTE,
            25 => Opcode::GT,
            26 => Opcode::GTE,
            27 => Opcode::Eq,
            28 => Opcode::Neq,
            29 => Opcode::MkArr,
            30 => Opcode::Store,
            31 => Opcode::Load,
            32 => Opcode::Return,
            _ => unreachable!()
        }
    }
}
