#[derive(Display, Debug, PartialEq)]
#[repr(u8)]
pub enum Opcode {
    Constant = 0,
    Nil,
    IConst0,
    IConst1,
    IConst2,
    IConst3,
    IConst4,
    IAdd,
    ISub,
    IMul,
    IDiv,
    FAdd,
    FSub,
    FMul,
    FDiv,
    IMod,
    FMod,
    I2F,
    F2I,
    Invert,
    StrConcat,
    T,
    F,
    And,
    Or,
    Negate,
    Coalesce,
    LT,
    LTE,
    GT,
    GTE,
    Eq,
    Neq,
    ArrMk,
    ArrLoad,
    ArrSlc,
    GStore,
    LStore0,
    LStore1,
    LStore2,
    LStore3,
    LStore4,
    LStore,
    GLoad,
    LLoad0,
    LLoad1,
    LLoad2,
    LLoad3,
    LLoad4,
    LLoad,
    Jump,
    JumpIfF,
    JumpB,
    Invoke,
    Pop,
    Return,
}

impl From<u8> for Opcode {
    fn from(i: u8) -> Self {
        match i {
            0 => Opcode::Constant,
            1 => Opcode::Nil,
            2 => Opcode::IConst0,
            3 => Opcode::IConst1,
            4 => Opcode::IConst2,
            5 => Opcode::IConst3,
            6 => Opcode::IConst4,
            7 => Opcode::IAdd,
            8 => Opcode::ISub,
            9 => Opcode::IMul,
            10 => Opcode::IDiv,
            11 => Opcode::FAdd,
            12 => Opcode::FSub,
            13 => Opcode::FMul,
            14 => Opcode::FDiv,
            15 => Opcode::IMod,
            16 => Opcode::FMod,
            17 => Opcode::I2F,
            18 => Opcode::F2I,
            19 => Opcode::Invert,
            20 => Opcode::StrConcat,
            21 => Opcode::T,
            22 => Opcode::F,
            23 => Opcode::And,
            24 => Opcode::Or,
            25 => Opcode::Negate,
            26 => Opcode::Coalesce,
            27 => Opcode::LT,
            28 => Opcode::LTE,
            29 => Opcode::GT,
            30 => Opcode::GTE,
            31 => Opcode::Eq,
            32 => Opcode::Neq,
            33 => Opcode::ArrMk,
            34 => Opcode::ArrLoad,
            35 => Opcode::ArrSlc,
            36 => Opcode::GStore,
            37 => Opcode::LStore0,
            38 => Opcode::LStore1,
            39 => Opcode::LStore2,
            40 => Opcode::LStore3,
            41 => Opcode::LStore4,
            42 => Opcode::LStore,
            43 => Opcode::GLoad,
            44 => Opcode::LLoad0,
            45 => Opcode::LLoad1,
            46 => Opcode::LLoad2,
            47 => Opcode::LLoad3,
            48 => Opcode::LLoad4,
            49 => Opcode::LLoad,
            50 => Opcode::Jump,
            51 => Opcode::JumpIfF,
            52 => Opcode::JumpB,
            53 => Opcode::Invoke,
            54 => Opcode::Pop,
            55 => Opcode::Return,
            _ => unreachable!()
        }
    }
}

impl Opcode {
    pub fn expects_imm(&self) -> bool {
        match self {
            Opcode::Constant |
            Opcode::Jump |
            Opcode::JumpIfF |
            Opcode::JumpB |
            Opcode::ArrMk |
            Opcode::LStore |
            Opcode::LLoad |
            Opcode::Invoke => true,
            _ => false
        }
    }
}
